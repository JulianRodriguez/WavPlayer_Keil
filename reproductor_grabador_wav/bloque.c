void bloque_pintar(int x, int y, int color)
{
    int i, j;

    for (i = 0; i < BLOQUE_TAMANO; i++)
    {
        for (j = 0; j < BLOQUE_TAMANO; j++)
        {
            uint32_t c = definicion_bloque[i][j] & color;
            glcd_setpixel(x + j, y + i, c);                     
        }
    }    
}
